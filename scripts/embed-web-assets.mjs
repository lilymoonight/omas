#!/usr/bin/env node
// Walk dist/web/ and emit src/server/web-assets.gen.ts — a single TS module
// that base64-encodes every static asset. Loaded at server startup so the
// SPA can be served straight from the binary (Bun --compile bundles all
// imported text, but it doesn't ship arbitrary disk dirs).
//
// Node-only (no Bun-specific syntax) so the resulting module compiles under
// tsc and can be used both by the Node dev build and the Bun single binary.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const webDir = path.resolve(root, 'dist/web');
const outFile = path.resolve(root, 'src/server/web-assets.gen.ts');

const MIME = new Map([
  ['.html', 'text/html; charset=utf-8'],
  ['.js',   'application/javascript; charset=utf-8'],
  ['.mjs',  'application/javascript; charset=utf-8'],
  ['.css',  'text/css; charset=utf-8'],
  ['.json', 'application/json; charset=utf-8'],
  ['.map',  'application/json; charset=utf-8'],
  ['.svg',  'image/svg+xml'],
  ['.png',  'image/png'],
  ['.jpg',  'image/jpeg'],
  ['.jpeg', 'image/jpeg'],
  ['.gif',  'image/gif'],
  ['.ico',  'image/x-icon'],
  ['.woff', 'font/woff'],
  ['.woff2','font/woff2'],
  ['.ttf',  'font/ttf'],
  ['.otf',  'font/otf'],
]);

function walk(dir) {
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full));
    else if (entry.isFile()) out.push(full);
  }
  return out;
}

const STUB = `// AUTO-GENERATED by scripts/embed-web-assets.mjs — do not edit.
// Stub: no dist/web yet. Run \`npm run build\` (or \`npm run build:web\` first) to embed assets.

export type EmbeddedAsset = { mime: string; body: Buffer };

export const embeddedAssets = {
  get(_url: string): EmbeddedAsset | undefined { return undefined; },
  has(_url: string): boolean { return false; },
  urls(): string[] { return []; },
  count: 0,
};
`;

if (!fs.existsSync(webDir)) {
  fs.writeFileSync(outFile, STUB);
  console.log(`wrote stub ${outFile} (dist/web missing — server will serve from disk when available)`);
  process.exit(0);
}

const files = walk(webDir).sort();
const entries = [];
let totalRaw = 0;
let totalB64 = 0;
for (const file of files) {
  // Skip source maps from the binary — they're large and useless in production
  if (file.endsWith('.map')) continue;
  const rel = '/' + path.relative(webDir, file).split(path.sep).join('/');
  const ext = path.extname(file).toLowerCase();
  const mime = MIME.get(ext) ?? 'application/octet-stream';
  const buf = fs.readFileSync(file);
  const b64 = buf.toString('base64');
  totalRaw += buf.length;
  totalB64 += b64.length;
  entries.push({ url: rel, mime, b64 });
}

const body = `// AUTO-GENERATED by scripts/embed-web-assets.mjs — do not edit.
// ${entries.length} files / ${(totalRaw / 1024).toFixed(1)} KiB raw / ${(totalB64 / 1024).toFixed(1)} KiB base64.

export type EmbeddedAsset = { mime: string; body: Buffer };

const RAW: Record<string, { mime: string; b64: string }> = {
${entries.map((e) => `  ${JSON.stringify(e.url)}: { mime: ${JSON.stringify(e.mime)}, b64: ${JSON.stringify(e.b64)} },`).join('\n')}
};

let _decoded: Record<string, EmbeddedAsset> | null = null;
function decode(): Record<string, EmbeddedAsset> {
  if (_decoded) return _decoded;
  const out: Record<string, EmbeddedAsset> = {};
  for (const [url, { mime, b64 }] of Object.entries(RAW)) {
    out[url] = { mime, body: Buffer.from(b64, 'base64') };
  }
  _decoded = out;
  return out;
}

export const embeddedAssets = {
  get(url: string): EmbeddedAsset | undefined { return decode()[url]; },
  has(url: string): boolean { return url in RAW; },
  urls(): string[] { return Object.keys(RAW); },
  count: ${entries.length},
};
`;

fs.writeFileSync(outFile, body);
console.log(`wrote ${outFile}`);
console.log(`  ${entries.length} files, ${(totalRaw / 1024).toFixed(1)} KiB raw, ${(totalB64 / 1024).toFixed(1)} KiB encoded`);
